var nota = prompt("Ingrese su calificacion");


if (parseInt(nota)<=3){
    alert("Su desempeño fué muy bajo")

} else if (parseInt(nota)<=5){
    alert("Su desempeño fué regular")
} else if (parseInt(nota)<=7) {
    alert("Su desempeño fué muy bueno")
} else {
    alert("La nota no es válida")
}


